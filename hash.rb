#Type of hashes
#Normal hash
_hash = {"hola" => "0", "hola_two" => "1"}

#Puts with method length => returns 2, NOTE: In ruby always start in 0.
puts _hash.length

#Symbol hash
_hash_two = {hello: "hello", world: "world", year: 2019}

#Puts with method length again => returns 3, NOTE: In ruby always start in 0.
puts _hash_two.length